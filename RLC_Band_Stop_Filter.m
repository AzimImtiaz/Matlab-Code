clc;
clear all;
close all;
R=64;
L=0.01273;
C=4.8579e-10;
fc=64e3;
num = [L*C,0,1];
den = [L*C,C*R,1];
w=logspace(log10(2*pi*63000),log10(2*pi*65000),80);
h=freqs(num,den,w);
f=w/(2*pi);
mag=20*log10(abs(h));
semilogx(f,mag,'b','LineWidth',1.5);
grid on;
xlabel('Frequency(Hz)');
ylabel('Gain(dB)');
title('Frequency Response of RLC Band Stop Filter');
xline(fc+200,'r',LineWidth=1);
xline(fc-200,'r',LineWidth=1);
yline(-3,'r',LineWidth=1);
