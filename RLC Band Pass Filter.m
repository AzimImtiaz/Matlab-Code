clc;
clear all;
close all;
R=64;
L=0.0254;
C=2.4347e-10;
fc=64e3;
num = [0,C*R,0];
den = [L*C,C*R,1];
w=logspace(log10(2*pi*62000),log10(2*pi*66000),500);
h=freqs(num,den,w);
f=w/(2*pi);
mag=20*log10(abs(h));
semilogx(f,mag,'b','LineWidth',1.5);
grid on;
xlabel('Frequency(Hz)');
ylabel('Gain(dB)');
title('Frequency Response of RLC Band-Pass Filter');
xline(fc+200,'r',LineWidth=1);
xline(fc-200,'r',LineWidth=1);
yline(-3,'r',LineWidth=1);
